import Foundation

extension CGFloat {
	static public let zero: CGFloat = 0.0
	// MARK: Radius
	/// Small corner radius 8
	static public let radiusSmall: CGFloat = 8.0

	/// Default corner radius 10
	static public let radiusBase: CGFloat = 10

	// MARK: Padding
	/// Small padding 4
	static public let paddingSmall: CGFloat = 4
	/// Default padding 8
	static public let paddingBase: CGFloat = 8
	/// Large padding 16
	static public let paddingLarge: CGFloat = 16
	/// XLarge padding 24
	static public let paddingXLarge: CGFloat = 24

	// MARK: Spacing
	/// Small padding 4
	static public let spacingSmall: CGFloat = 4
	/// Default padding 8
	static public let spacingBase: CGFloat = 8
	/// Large padding 16
	static public let spacingLarge: CGFloat = 16

	// MARK: Content Margins
	/// Default padding 8
	static public let contentMarginsBase: CGFloat = 16
}
